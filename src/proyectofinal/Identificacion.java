    public void compruebaAdmin(){
        try {
            db.conexion("root", "");
            String usuario = this.jTextField1.getText();
            String password = this.jTextField2.getText();
            if(db.compruebaBDadmin(usuario, password)) {
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null,"El usuario/password no existen.");
            }
            db.desconexion();
        } catch (Exception ex) {
            Logger.getLogger(Identificacion.class.getName()).log(Level.SEVERE, null, ex);
            db.desconexion();
        }
        
        
    }


